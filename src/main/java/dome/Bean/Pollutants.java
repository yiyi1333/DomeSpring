package dome.Bean;

public class Pollutants {
    private double pm2;
    private double pm10;
    private double so2;
    private double no2;
    private double co;
    private double o3;

    public double getPm2() {
        return pm2;
    }

    public void setPm2(double pm2) {
        this.pm2 = pm2;
    }

    public double getPm10() {
        return pm10;
    }

    public void setPm10(double pm10) {
        this.pm10 = pm10;
    }

    public double getSo2() {
        return so2;
    }

    public void setSo2(double so2) {
        this.so2 = so2;
    }

    public double getNo2() {
        return no2;
    }

    public void setNo2(double no2) {
        this.no2 = no2;
    }

    public double getCo() {
        return co;
    }

    public void setCo(double co) {
        this.co = co;
    }

    public double getO3() {
        return o3;
    }

    public void setO3(double o3) {
        this.o3 = o3;
    }
}
